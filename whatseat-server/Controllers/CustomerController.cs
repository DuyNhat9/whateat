public async Task<IActionResult> AddOrder([FromBody] OrderCreateRequest request)
{
    Guid userId = new Guid(User.FindFirst("Id")?.Value);  // User Identification: Extracts user ID from JWT token -> Next: Initializes classifiedOrders dictionary

    Dictionary<int, Order> classifiedOrders = new Dictionary<int, Order>();  // Data Structure: Initializes dictionary to manage orders by store ID -> Next: Loops through product requests

    foreach (var productReq in request.ProductList)  // Loop: Iterates over each product in the request -> Next: Fetch product details
    {
        var product = await _productService.FindProductById(productReq.ProductId);  // Database Access: Fetches product by ID -> Next: Check product stock

        if (product.InStock < productReq.Quantity)  // Condition: Checks if requested quantity is available -> Next: Return BadRequest or continue
        {
            return BadRequest(new  // Response: Sends error if insufficient stock -> Ends method execution
            {
                productId = product.ProductId,
                message = $"Insufficient quantity of in store {product.Name}"
            });
        }
        try  // Error Handling: Starts try block to handle exceptions -> Next: Order processing logic
        {
            // The actual order processing logic would continue here...
                int storeId = product.Store.StoreId; // üÜî Fetch Store ID -> üîú Next: Check if order for store exists
                Order currentOrder;// üì¶ Initialize order variable
                if (classifiedOrders.ContainsKey(storeId)) // üîÑ Check: If order for this store already started
                {
                     // üîÑ Use existing order -> üîú Next: Add product to order
                    currentOrder = classifiedOrders[storeId];
                }
                else// üÜï Create new order if none exists for this store
                {
                    // üì¶ Create new Order object
                    currentOrder = new Order
                    {
                        // üßë Link customer to order
                        Customer = await _customerService.FindCustomerByIdAsync(userId),
                        Store = product.Store,// üè™ Link store to order
                        OrderDetails = new List<OrderDetail>(),// üì¶ Initialize order details list
                        CreatedOn = DateTime.UtcNow,// üìÖ Set order creation date
                        ShippingInfo = _context.ShippingInfos.FirstOrDefault(si => si.ShippingInfoId == request.ShippingInfoId),// üì¶ Link shipping info to order
                        PaymentMethod = _context.PaymentMethods.FirstOrDefault(pm => pm.PaymentMethodId == request.PaymentMethodId),// üì¶ Link payment method to order
                    };

                    currentOrder.ShippingFee = await _orderService.CalculateFee(new OrderShippingFeeRequest // üîÑ Calculate shipping fee
                    {
                        // FromDistrictId = product.Store.DistrictCode,
                        ToDistrictId = currentOrder.ShippingInfo.DistrictCode,// üì¶ Link shipping info to order
                        ToWardCode = currentOrder.ShippingInfo.DistrictCode,// üì¶ Link shipping info to order
                        ServiceId = request.ServiceId// üì¶ Link shipping info to order
                    });

                    if (currentOrder.ShippingFee < 0)// ‚ùå Check if shipping fee calculation failed
                    {
                        return BadRequest(new { Message = "Cannot calculate shipping fee" });// üö´ Return error if shipping fee calculation failed
                    }
                    classifiedOrders.Add(storeId, currentOrder);// ‚ûï Add new order to dictionary
                }

                product.InStock -= productReq.Quantity;// üìâ Decrease stock count
               // ‚ûï Add product to order details
                currentOrder.OrderDetails.Add(new OrderDetail 
                {
                    ProductId = productReq.ProductId,// üÜî Product ID
                    Quantity = productReq.Quantity, // üìä Quantity
                    Price = product.BasePrice// üí∏ Price
                });

            }
            catch (NullReferenceException ex)// üö® Handle null reference exception
            {
                // üö® Return error if null reference exception occurs
                return this.StatusCode(StatusCodes.Status500InternalServerError, new { message = ex.Message });
            }
        }

        foreach (var order in classifiedOrders)// üîÑ Loop through classified orders
        {
            _logger.LogInformation($"{order.Key}");// üî• Log store ID
        }
        // üìù Initialize list to store orders.
        List<Order> orders = new List<Order>();
        // üî• Fetch all orders from dictionary
        Dictionary<int, Order>.ValueCollection values = classifiedOrders.Values;
        // üî• Fetch all orders from dictionary
        using (var dbContextTransaction = await _context.Database.BeginTransactionAsync())
        {
            foreach (var val in values)// üî• Fetch all orders from dictionary
            {
                // üî• Add order to orders list
                orders.Add(val);
                // üî• Update order status to waiting
                OrderStatusHistory orderStatusWaiting = await _orderService.UpdateStatus(val, OrderStatusConstant.Waiting);
            }
            await dbContextTransaction.CommitAsync();// ‚úîÔ∏è Commit the transaction to the database.
        }

        // await _context.Orders.AddRangeAsync(orders);
        // await _context.SaveChangesAsync();


        return Ok(orders);
    }

