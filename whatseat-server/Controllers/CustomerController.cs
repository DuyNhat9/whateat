    [HttpPut] // üìå Route: /api/customer/update-calo-per-day
    [Route("update-calo-per-day")] // üìå Route: /api/customer/update-calo-per-day
    [Authorize(AuthenticationSchemes = JwtBearerDefaults.AuthenticationScheme, Roles = "customer")] // üîë Authorization: Only customers can update calo per day
    public async Task<IActionResult> UpdateCaloPerDay([FromBody] AddCaloRequest request) // üî• Update calo per day
    {
        var now = DateTime.Now;// üî• Fetch current date time
        var birthDay = Int32.Parse(request.Year);// üî• Fetch birth day from request
        var age = now.Year - birthDay;// üî• Calculate age
        Guid userId = new Guid(User.FindFirst("Id")?.Value);// üî• Fetch user ID from JWT token

        var brm = request.Gender == "male" ? // üî• Calculate BMR
            66 + (6.3 * request.Weight * 2.20462262) + (12.9 * request.Height * 0.393700787) + (6.8 * age) : // üî• Calculate BMR for male
            66.5 + (4.3 * request.Weight * 2.20462262) + (4.7 * request.Height * 0.393700787) + (4.7 * age); // üî• Calculate BMR for female

        var calorie = (float)Math.Round(brm * float.Parse(request.PAL), 2); // üî• Calculate calorie
        var customer = await _customerService.FindCustomerByIdAsync(userId);// üî• Fetch customer by ID
        customer.KcalPerDay = calorie * request.Person;// üî• Update calo per day
        customer.Allergy = request.Allergy;// üî• Update allergy
        await _context.SaveChangesAsync();// üî• Save changes to the database

        return Ok(new UpdateCaloResponse(customer.KcalPerDay, customer.Allergy));// üî• Return calo per day to the client
    }


