                totalLike = item.totalLike, // üî• Fetch recipe total like
                videoUrl = item.videoUrl, // üî• Fetch recipe video url
                RecipeTypes = await _context.RecipeRecipeTypes.Where(rrt => rrt.RecipeId == item.RecipeId).ToListAsync(), // üî• Fetch recipe types
                Level = item.Level, // üî• Fetch recipe level
                Images = _recipeService.ConvertJsonToPhotos(item.ThumbnailUrl) // üî• Fetch recipe images
            });
        }

        return Ok(recipeRes);// üî• Return recipe response to the client 
    }

