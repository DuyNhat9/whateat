        };

        foreach (var item in request.RecipeTypeIds) // üî• Fetch all recipe types from request
        {
            var recipeType = await _context.RecipeTypes // üî• Fetch recipe type by ID
            .FirstOrDefaultAsync(pc => pc.RecipeTypeId == item); // üî• Fetch recipe type by ID

            RecipeRecipeType recipeRecipeType = new RecipeRecipeType // üî• Initialize recipe recipe type
            {
                RecipeType = recipeType,// üî• Update recipe type
                Recipe = recipe // üî• Update recipe
            };        

            if (recipeType is not null) // üî• Check if recipe type is not null
            {
                recipeTypes.Add(recipeRecipeType);// üî• Add recipe recipe type to list
            }
        }

        await _context.RecipeRecipeTypes.AddRangeAsync(recipeTypes); // üî• Add recipe recipe types to database
        await _context.Recipes.AddAsync(recipe);// üî• Add recipe to database
        await _context.SaveChangesAsync();// üî• Save changes to the database
